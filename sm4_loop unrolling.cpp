//循环左移n位算法
u32 LoopLeft(unsigned long Input, int n) {
	u32 Output;
	Output = (Input << n) | (Input >> (32 - n));
	return Output;
}

//t变换
unsigned int Transt_int(unsigned int temp) {
	return Sbox[temp >> 4][temp & 0xf];	
}

u32 Transt(u32 rki) {
	u32 rki_aftert = 0;		//输出t置换之后的rki
	rki_aftert |= (unsigned long)Transt_int((unsigned int)(rki >> 24) & 0xff);
	rki_aftert <<= 8;
	rki_aftert |= (unsigned long)Transt_int((unsigned int)(rki >> 16) & 0xff);
	rki_aftert <<= 8;
	rki_aftert |= (unsigned long)Transt_int((unsigned int)(rki >> 8) & 0xff);
	rki_aftert <<= 8;
	rki_aftert |= (unsigned long)Transt_int((unsigned int)rki & 0xff);
	return rki_aftert;
}

//L'变换
u32 TransL_1(u32 rki_aftert) {
	return rki_aftert ^ LoopLeft(rki_aftert, 13) ^ LoopLeft(rki_aftert, 23);
}

//T'置换，由t变换(Sbox)和L'变换复合
u32 TransT_1(u32 temp) {
	return TransL_1(Transt(temp));
}

//L变换
u32 TransL(u32 temp_aftert) {
	return temp_aftert ^ LoopLeft(temp_aftert, 2) ^ LoopLeft(temp_aftert, 10) ^ LoopLeft(temp_aftert, 18) ^ LoopLeft(temp_aftert, 24);
}

//T置换
u32 TransT(u32 temp) {
	return TransL(Transt(temp));
}

//F轮函数
u32 Ffunc(u32 X0, u32 X1, u32 X2, u32 X3, u32 rk) {
	return X0 ^ TransT(X1 ^ X2 ^ X3 ^ rk);
}

u32 generateMK(u32 key0, u32 key1, u32 key2, u32 key3) {
	u32 MKi;
	MKi = (key0 << 24) | (key1 << 16) | (key2 << 8) | key3;
	return MKi;
}

void generaterk(u32 MK[4],int n) {
	u32 K[4];
	int i;
	for (i = 0; i < 4; i++) {
		K[i] = MK[i] ^ FK[i];
	}
	rk1[0] = K[0] ^ TransT_1(K[1] ^ K[2] ^ K[3] ^ CK[0]); 
	rk1[1] = K[1] ^ TransT_1(K[2] ^ K[3] ^ rk1[0] ^ CK[1]);	
	rk1[2] = K[2] ^ TransT_1(K[3] ^ rk1[0] ^ rk1[1] ^ CK[2]);
	rk1[3] = K[3] ^ TransT_1(rk1[0] ^ rk1[1] ^ rk1[2] ^ CK[3]);
	for (i = 4; i < 32; )//循环展开
	{
		rk1[i] = rk1[i - 4] ^ TransT_1(rk1[i - 3] ^ rk1[i - 2] ^ rk1[i - 1] ^ CK[i]); i++;
		rk1[i] = rk1[i - 4] ^ TransT_1(rk1[i - 3] ^ rk1[i - 2] ^ rk1[i - 1] ^ CK[i]); i++;
		rk1[i] = rk1[i - 4] ^ TransT_1(rk1[i - 3] ^ rk1[i - 2] ^ rk1[i - 1] ^ CK[i]); i++;
		rk1[i] = rk1[i - 4] ^ TransT_1(rk1[i - 3] ^ rk1[i - 2] ^ rk1[i - 1] ^ CK[i]); i++;
		rk1[i] = rk1[i - 4] ^ TransT_1(rk1[i - 3] ^ rk1[i - 2] ^ rk1[i - 1] ^ CK[i]); i++;
		rk1[i] = rk1[i - 4] ^ TransT_1(rk1[i - 3] ^ rk1[i - 2] ^ rk1[i - 1] ^ CK[i]); i++;
		rk1[i] = rk1[i - 4] ^ TransT_1(rk1[i - 3] ^ rk1[i - 2] ^ rk1[i - 1] ^ CK[i]); i++;
		rk1[i] = rk1[i - 4] ^ TransT_1(rk1[i - 3] ^ rk1[i - 2] ^ rk1[i - 1] ^ CK[i]); i++;
	}
	if (n == 1) 
		for (int j = 0; j < 32; j++)
		{
			rk[j] = rk1[31 - j];
		}
	else
		for (int j = 0; j < 32; j++)
		{
			rk[j] = rk1[j];
		}
}

void SM4(u32 X[4], u32 MK[4],int n) {
	u32 temp;
	generaterk(MK , n);
	u32 Plain[4];
	for (int i = 0; i < 4; i++) {
		Plain[i] = X[i];
	}
	for (int i = 0; i < 32; i++) {
		temp = Ffunc(Plain[0], Plain[1], Plain[2], Plain[3], rk[i]);
		Plain[0] = Plain[1];
		Plain[1] = Plain[2];
		Plain[2] = Plain[3];
		Plain[3] = temp;
	}
	//0和3换
	Plain[3] ^= Plain[0];
	Plain[0] ^= Plain[3];
	Plain[3] ^= Plain[0];
	//1和2换
	Plain[1] ^= Plain[2];
	Plain[2] ^= Plain[1];
	Plain[1] ^= Plain[2];
	for (int i = 0; i < 4; i++) {
		plain[i] = Plain[i];
	}
}

